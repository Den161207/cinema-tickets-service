def show_menu():
    print("\n--- Сервіс купівлі квитків в кіно ---")
    print("1. Переглянути афішу (список фільмів)")
    print("2. Купити квиток")
    print("3. Особистий кабінет (історія замовлень)")
    print("4. Вихід")
    print("---------------------------------------")

def main():
    is_running = True
    
    while is_running:
        show_menu()
        choice = input("Оберіть дію (1-4): ")
        
        if choice == '1':
            print("\nТут буде список фільмів...")
        elif choice == '2':
            print("\nТут буде процес вибору місця та купівлі...")
        elif choice == '3':
            print("\nТут буде інформація про користувача...")
        elif choice == '4':
            print("\nДякуємо за використання сервісу! До побачення.")
            is_running = False
        else:
            print("\nНевірна команда. Будь ласка, введіть число від 1 до 4.")

if __name__ == "__main__":
    main()